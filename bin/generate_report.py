#!/usr/bin/env python3
"""
Generate a clinician-readable report from prioritized variants.

Consumes the JSON produced by prioritize_variants.py and emits:
  <prefix>.report.html   a self-contained (no external assets) HTML report
  <prefix>.report.json   the structured report payload
  <prefix>.fhir.json     a minimal FHIR R4 Bundle (DiagnosticReport + Observations)

Design principles (per project rules):
  * Works fully offline with NO LLM - a deterministic, templated narrative is
    always produced (graceful degradation, zero empty outputs).
  * An optional LLM narrative can ENRICH the report (--llm), but if no backend is
    available the report still renders with the templated narrative; nothing is
    fabricated.
  * Research-use-only labelling throughout; this is not a diagnostic device.

Stdlib only, so it runs in a minimal environment and is unit-testable.
"""

from __future__ import annotations

import argparse
import html
import json
import logging
import sys
from pathlib import Path

logging.basicConfig(
    stream=sys.stderr,
    level=logging.INFO,
    format="[generate_report] %(levelname)s: %(message)s",
)
log = logging.getLogger("generate_report")

DISCLAIMER = (
    "RESEARCH USE ONLY. This report is generated by an automated pipeline for "
    "research and development purposes. It is not a validated diagnostic device "
    "and must not be used for clinical decision-making. Variant tiers reflect "
    "predicted functional impact, not a clinical ACMG classification."
)


def load_prioritized(path: Path) -> dict:
    data = json.loads(path.read_text(encoding="utf-8"))
    data.setdefault("variants", [])
    data.setdefault("sample", "sample")
    data.setdefault("tier_counts", {})
    return data


def build_narrative(summary: dict) -> str:
    """Deterministic, factual summary sentence(s). No model required."""
    variants = summary.get("variants", [])
    counts = summary.get("tier_counts", {})
    n = len(variants)
    if n == 0:
        return ("No variants were reported for this sample after calling and "
                "filtering.")
    high = int(counts.get("1", 0))
    moderate = int(counts.get("2", 0))
    genes = sorted({v.get("gene", "") for v in variants
                    if v.get("tier") in (1, 2) and v.get("gene")})
    parts = [f"{n} variant(s) were reported."]
    if high:
        parts.append(f"{high} were predicted HIGH impact.")
    if moderate:
        parts.append(f"{moderate} were predicted MODERATE impact.")
    if genes:
        parts.append("Genes with higher-impact variants: " + ", ".join(genes) + ".")
    return " ".join(parts)


def maybe_llm_narrative(summary: dict, enable_llm: bool) -> str:
    """Optional LLM enrichment hook. Falls back to the templated narrative.

    No LLM backend is bundled; when --llm is requested but none is wired in, we
    log and degrade rather than fabricating a narrative.
    """
    templated = build_narrative(summary)
    if not enable_llm:
        return templated
    log.warning("LLM narrative requested but no backend configured; "
                "using the deterministic templated narrative.")
    return templated


def _fmt_af(af) -> str:
    """Render a gnomAD AF compactly, or '' when absent."""
    if af in (None, ""):
        return ""
    try:
        return f"{float(af):.3g}"
    except (TypeError, ValueError):
        return html.escape(str(af))


def _row_html(v: dict) -> str:
    cells = [
        v.get("tier", ""),
        html.escape(str(v.get("gene", ""))),
        f"{html.escape(str(v.get('chrom', '')))}:{v.get('pos', '')}",
        html.escape(f"{v.get('ref', '')}>{v.get('alt', '')}"),
        html.escape(str(v.get("impact", ""))),
        html.escape(str(v.get("effect", ""))),
        html.escape(str(v.get("hgvs_p", ""))),
        html.escape(str(v.get("genotype", ""))),
        html.escape(str(v.get("filter", ""))),
        html.escape(str(v.get("acmg_classification", ""))),
        html.escape(str(v.get("clinvar_sig", ""))),
        _fmt_af(v.get("gnomad_af")),
        html.escape(str(v.get("rsid", ""))),
    ]
    return "<tr>" + "".join(f"<td>{c}</td>" for c in cells) + "</tr>"


def render_html(summary: dict, narrative: str) -> str:
    sample = html.escape(str(summary.get("sample", "sample")))
    counts = summary.get("tier_counts", {})
    rows = "\n".join(_row_html(v) for v in summary.get("variants", []))
    if not rows:
        rows = ('<tr><td colspan="13" style="text-align:center">'
                'No variants reported.</td></tr>')
    tier_summary = " &nbsp;|&nbsp; ".join(
        f"Tier {t}: {int(counts.get(str(t), 0))}" for t in (1, 2, 3, 4)
    )
    acmg_counts = summary.get("acmg_counts", {})
    acmg_summary = (" &nbsp;|&nbsp; ".join(
        f"{html.escape(str(k))}: {int(n)}" for k, n in acmg_counts.items()
    ) if acmg_counts else "")
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Variant Report - {sample}</title>
<style>
  body {{ font-family: system-ui, Arial, sans-serif; margin: 2rem; color: #1a1a1a; }}
  h1 {{ font-size: 1.5rem; }}
  .banner {{ background: #fff4e5; border: 1px solid #ffb020; padding: .75rem 1rem;
             border-radius: 6px; font-size: .85rem; margin: 1rem 0; }}
  .summary {{ background: #f5f7fa; padding: .75rem 1rem; border-radius: 6px; }}
  table {{ border-collapse: collapse; width: 100%; margin-top: 1rem; font-size: .85rem; }}
  th, td {{ border: 1px solid #d0d7de; padding: .4rem .55rem; text-align: left; }}
  th {{ background: #eaeef2; }}
  tr:nth-child(even) td {{ background: #fafbfc; }}
  footer {{ margin-top: 2rem; font-size: .75rem; color: #666; }}
</style>
</head>
<body>
  <h1>NGS Variant Report</h1>
  <p><strong>Sample:</strong> {sample}</p>
  <div class="banner">{html.escape(DISCLAIMER)}</div>
  <div class="summary">
    <p><strong>Summary.</strong> {html.escape(narrative)}</p>
    <p>{tier_summary}</p>
    {f'<p><strong>ACMG-style class (research-use):</strong> {acmg_summary}</p>' if acmg_summary else ''}
  </div>
  <table>
    <thead>
      <tr><th>Tier</th><th>Gene</th><th>Locus</th><th>Change</th><th>Impact</th>
      <th>Effect</th><th>HGVS.p</th><th>Genotype</th><th>Filter</th>
      <th>ACMG-style</th><th>ClinVar</th><th>gnomAD AF</th><th>dbSNP</th></tr>
    </thead>
    <tbody>
      {rows}
    </tbody>
  </table>
  <footer>Generated by ngs-variant-pipeline. Research use only.</footer>
</body>
</html>
"""


def build_fhir(summary: dict) -> dict:
    """A minimal, structurally-valid FHIR R4 Bundle. Research-use labelled.

    One DiagnosticReport plus one Observation per variant. This is a lightweight
    representation, not a full Genomics-Reporting IG conformant document.
    """
    sample = str(summary.get("sample", "sample"))
    entries: list[dict] = []

    obs_refs = []
    for i, v in enumerate(summary.get("variants", []), start=1):
        obs_id = f"variant-{i}"
        obs_refs.append({"reference": f"Observation/{obs_id}"})
        entries.append({
            "resource": {
                "resourceType": "Observation",
                "id": obs_id,
                "status": "final",
                "category": [{"coding": [{
                    "system": "http://terminology.hl7.org/CodeSystem/observation-category",
                    "code": "laboratory"}]}],
                "code": {"coding": [{
                    "system": "http://loinc.org", "code": "69548-6",
                    "display": "Genetic variant assessment"}]},
                "valueString": (f"{v.get('gene', '')} {v.get('chrom', '')}:"
                                f"{v.get('pos', '')} {v.get('ref', '')}>"
                                f"{v.get('alt', '')} "
                                f"[{v.get('impact', '')}/{v.get('effect', '')}] "
                                f"tier {v.get('tier', '')}"
                                + (f" ACMG-style: {v['acmg_classification']}"
                                   if v.get('acmg_classification') else "")),
            }
        })

    entries.insert(0, {
        "resource": {
            "resourceType": "DiagnosticReport",
            "id": "genetics-report",
            "status": "final",
            "category": [{"coding": [{
                "system": "http://terminology.hl7.org/CodeSystem/v2-0074",
                "code": "GE", "display": "Genetics"}]}],
            "code": {"coding": [{
                "system": "http://loinc.org", "code": "51969-4",
                "display": "Genetic analysis report"}]},
            "subject": {"display": sample},
            "conclusion": build_narrative(summary) + " RESEARCH USE ONLY.",
            "result": obs_refs,
        }
    })

    return {"resourceType": "Bundle", "type": "collection", "entry": entries}


def write_reports(summary: dict, narrative: str, prefix: Path) -> None:
    prefix.parent.mkdir(parents=True, exist_ok=True)
    (prefix.parent / f"{prefix.name}.report.html").write_text(
        render_html(summary, narrative), encoding="utf-8")
    report_json = {
        "sample": summary.get("sample"),
        "narrative": narrative,
        "tier_counts": summary.get("tier_counts", {}),
        "acmg_counts": summary.get("acmg_counts", {}),
        "total_variants": summary.get("total_variants", len(summary.get("variants", []))),
        "variants": summary.get("variants", []),
        "disclaimer": DISCLAIMER,
    }
    (prefix.parent / f"{prefix.name}.report.json").write_text(
        json.dumps(report_json, indent=2), encoding="utf-8")
    (prefix.parent / f"{prefix.name}.fhir.json").write_text(
        json.dumps(build_fhir(summary), indent=2), encoding="utf-8")


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Generate a variant report.")
    p.add_argument("prioritized", type=Path, help="Prioritized variants JSON")
    p.add_argument("--prefix", type=Path, required=True,
                   help="Output path prefix (e.g. results/report/sample1)")
    p.add_argument("--llm", action="store_true",
                   help="Request LLM narrative enrichment (falls back if unavailable)")
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    if not args.prioritized.is_file():
        log.error("prioritized JSON not found: %s", args.prioritized)
        return 1
    summary = load_prioritized(args.prioritized)
    narrative = maybe_llm_narrative(summary, args.llm)
    write_reports(summary, narrative, args.prefix)
    log.info("wrote report for sample %s (%d variants)",
             summary.get("sample"), len(summary.get("variants", [])))
    return 0


if __name__ == "__main__":
    sys.exit(main())
